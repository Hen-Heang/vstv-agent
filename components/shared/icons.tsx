import React from 'react'

// Base icon props interface
export interface IconProps {
  className?: string
  size?: number
  color?: string
}

// Create a base icon component with consistent props
const createIcon = (IconComponent: React.ComponentType<React.SVGProps<SVGSVGElement>>) => {
  const Icon = React.forwardRef<SVGSVGElement, IconProps>(({ 
    className = '', 
    size = 24, 
    color = 'currentColor',
    ...props 
  }, ref) => (
    <IconComponent
      ref={ref}
      className={className}
      width={size}
      height={size}
      color={color}
      {...props}
    />
  ))
  
  Icon.displayName = `Icon`
  return Icon
}

